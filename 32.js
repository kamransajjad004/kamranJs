//objects in java script

//non primitive
// object uses key value pair
// left side is key
//right side is value
//to access value use the key
//ex: fname : "kamran"
// key : value
//add , after each value

let person = {
fname : "kamran",
phone: 8519897600,
age : 10,
isAlive : true,
health : undefined,
sleeping : null

}
console.log(person);

//How to acess object keys
// dot notation

console.log(person.fname);
console.log(phone.age);

// using bracket notation

console.log(person["fname"]);

